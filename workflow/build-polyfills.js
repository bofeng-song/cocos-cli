/**
 * 构建预览用的 polyfills bundle
 * 
 * 包含:
 * - core-js-bundle (ES 标准 API 垫片)
 * - regenerator-runtime (async/await 支持)
 * 
 * 输出到 static/web/polyfills.bundle.js
 */
const path = require('path');
const fs = require('fs-extra');

const outDir = path.join(__dirname, '..', 'static', 'web');
fs.ensureDirSync(outDir);

const outFile = path.join(outDir, 'polyfills.bundle.js');

console.log('📦 Building polyfills bundle...');

// 1. core-js-bundle (prebuilt)
const coreJsPath = path.join(require.resolve('core-js-bundle/package.json'), '..', 'minified.js');
const coreJsCode = fs.readFileSync(coreJsPath, 'utf8');
console.log(`  ✅ core-js-bundle: ${(coreJsCode.length / 1024).toFixed(1)} KB`);

// 2. regenerator-runtime
const regeneratorPath = require.resolve('regenerator-runtime/runtime');
const regeneratorCode = fs.readFileSync(regeneratorPath, 'utf8');
console.log(`  ✅ regenerator-runtime: ${(regeneratorCode.length / 1024).toFixed(1)} KB`);

// 3. Bundle together
const bundle = [
    '// Polyfills bundle for browser preview',
    '// Generated by workflow/build-polyfills.js',
    '// Includes: core-js (ES API polyfills) + regenerator-runtime (async/await)',
    '',
    coreJsCode,
    '',
    regeneratorCode,
].join('\n');

fs.writeFileSync(outFile, bundle, 'utf8');
console.log(`\n✅ Polyfills bundle → ${outFile}`);
console.log(`   Total: ${(bundle.length / 1024).toFixed(1)} KB`);
